package basic;

public class java_looping {
    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {

            if (i % 2 == 0) {
                System.out.println(i + " ini adalah bilangan genap ");
            }

            if (i % 2 != 0) {
                System.out.println(i + " ini adalah bilangan ganjil");
            }


        }
    }
}
